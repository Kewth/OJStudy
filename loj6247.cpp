#include <cstdio>
#include <algorithm>
#define debug(...) fprintf(stderr, __VA_ARGS__)

typedef long long ll;

struct {
	inline operator int () { int x; return scanf("%d", &x), x; }
	inline operator ll () { ll x; return scanf("%lld", &x), x; }
} read;

const int mod = 998244353;

inline ll power (ll x, int k) {
	if (k < 0) k += mod - 1;
	ll res = 1;
	while (k) {
		if (k & 1) (res *= x) %= mod;
		(x *= x) %= mod;
		k >>= 1;
	}
	return res;
}

int main () {
	ll n = read;
	int k = read;
	ll ans = 0, g = power(3, (mod - 1) / k), now = 1;
	for (int i = 0; i < k; i ++) {
		ans += power(1 + now, n % (mod - 1));
		(now *= g) %= mod;
	}
	ans %= mod;
	(ans *= mod - (mod - 1) / k) %= mod;
	printf("%lld\n", ans);
}

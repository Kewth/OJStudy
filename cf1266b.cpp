#include <cstdio>
#include <algorithm>
#define debug(...) fprintf(stderr, __VA_ARGS__)

typedef long long ll;

struct {
	inline operator int () { int x; return scanf("%d", &x), x; }
	inline operator ll () { ll x; return scanf("%lld", &x), x; }
} read;

int main () {
	int T = read;
	while (T --) {
		ll n = read;
		if (n > 14 and 1 <= n % 14 and n % 14 <= 6)
			puts("YES");
		else
			puts("NO");
	}
}
